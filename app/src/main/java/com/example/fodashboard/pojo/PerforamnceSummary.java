package com.example.fodashboard.pojo;

public class PerforamnceSummary {
    Double amount;
    int count;
    String fieldOfficerName;
    String image;
    String localeName;

    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getFieldOfficerName() {
        return fieldOfficerName;
    }

    public void setFieldOfficerName(String fieldOfficerName) {
        this.fieldOfficerName = fieldOfficerName;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getLocaleName() {
        return localeName;
    }

    public void setLocaleName(String localeName) {
        this.localeName = localeName;
    }
}
